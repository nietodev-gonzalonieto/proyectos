                            Es una disciplina que tiene en cuenta a toda la persona, y la concibe como a una unidad, y no
                            como una suma de sistemas, órganos y estructuras. Por lo que la ostepatia quiere llegar al
                            alivio de la dolencia a través de todo el equilibrio de nuestro cuerpo, más que al alivio orgánico
                            del punto de dolor indicado por el paciente.
                            Es por ello que busca la <strong>homeostasis</strong>, la capacidad que tiene el cuerpo humano para
                            encontrarse en un constante equilibrio, como la herramienta más importante de su trabajo.La osteopatía es una profesión que está reconocida por la Organización
                            Mundial de la Salud, es un conjunto de <strong>técnicas manuales</strong> fundamentadas en las leyes por
                            las que se rige la vida y los organismos vivos.<br>
                            Vamos a empezar por la gran pregunta, ¿En qué se diferencia la <strong> Osteopatía</strong>
                            con <strong>la fisioterapia</strong>?
                            La <strong>fisioterapia</strong> es una profesión que trata de rehabilitar aquellas zonas del
                            sistema musculo esquelético, respiratorio, suelo pélvico, pediátrica, neurológica, etc. Todos
                            estos sistemas forman parte de una misma persona pero se tratan de forma separada y solo
                            aquello que esta lesionado. Por otro lado, la <strong>Osteopatia</strong> tiene en cuenta
                            todo el organismo de la persona para poder llegar a su salud más plena.
                            Por ejemplo, si tienes un dolor lumbar no se limitará en hacer un masaje sobre la zona sino
                            que intentará buscar desde que parte del cuerpo se están produciendo esas tensiones que al
                            final provocan ese dolor en la columna lumbar. Ya que podría deberse a que no tuviéramos
                            una buena marcha, o una correcta higiene postural debido a otras lesiones como un esguince
                            de tobillo previo, o también podríamos tener un mal funcionamiento del movimiento de
                            nuestras vísceras y estas están íntimamente relacionadas con la lumbar.
