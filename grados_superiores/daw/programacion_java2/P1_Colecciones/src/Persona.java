
public class Persona {
	private String DNI;
	private int edat;
	
	public Persona(String dNI, int edat) {
		DNI = dNI;
		this.edat = edat;
	}

	public String getDNI() {
		return DNI;
	}

	public void setDNI(String dNI) {
		DNI = dNI;
	}

	public int getEdat() {
		return edat;
	}

	public void setEdat(int edat) {
		this.edat = edat;
	}
	
	
	
	
}
